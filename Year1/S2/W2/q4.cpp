using namespace std;
#include <iostream>

int main(int argc, char const *argv[]) {
  int a;
  int b;
  cin >> a >> b;
  cout << a << " + "  << b << " = " << a + b << endl;
  cout << a << " - "  << b << " = " << a - b << endl;
  cout << a << " * "  << b << " = " << a * b << endl;
  cout << a << " / "  << b << " = " << a / b << endl;
  cout << a << " % "  << b << " = " << a % b << endl;
  return 0;
}
